public class complex {
    public static void main(String[] args) {

        double csa1 = 1.1;
        double adit1 = 1.1;
        System.out.println("First complex number: " + csa1 + " + " + adit1 + "i");

        double csa2 = 5.2;
        double adit2 = 5.2;
        System.out.println("Second complex number: " + csa2 + " + " + adit2 + "i");

        double sumCsa = csa1 + csa2;
        double sumAdit = adit1 + adit2;

        System.out.println("The sum of the two complex numbers is: " + sumCsa + " + " + sumAdit + "i");

    }
}
