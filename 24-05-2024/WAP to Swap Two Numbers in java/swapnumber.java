public class swapnumber {
    public static void main(String[] args) {
        int  adit=10;
        int  csa=20;


        
       int ibm = adit;
        adit =  csa;
        csa = ibm;
        System.out.println(" swapping:");
        System.out.println("Adit=" + adit);
        System.out.println("Csa =" + csa);
     
    }
}
